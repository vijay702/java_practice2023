public class LargeNumberInArray {


    public static void main(String[] args) {

        int a[] = {10,20,30,60,40};

        int length = a.length;

        Integer highValue = a[0];

        for(int i=0; i<=length-1 ; i++){


            if(i>highValue){
                int value = i;

            }


        }
        System.out.println(value );
    }
}
